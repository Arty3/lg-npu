#!/usr/bin/env python3

"""
gen_headers.py - Generate C/UAPI headers from the register spec.

Usage:
    python3 tools/gen/gen_headers.py --out-dir sw/uapi

Produces:
    npu_regs.h  - Register offsets and bit-field macros
"""

from pathlib import Path
import argparse

# Identical source-of-truth as gen_addrmap.py
REGISTERS = [
    ("LGNPU_REG_CTRL",        0x0_0000),
    ("LGNPU_REG_STATUS",      0x0_0004),
    ("LGNPU_REG_DOORBELL",    0x0_0008),
    ("LGNPU_REG_IRQ_STATUS",  0x0_000C),
    ("LGNPU_REG_IRQ_ENABLE",  0x0_0010),
    ("LGNPU_REG_IRQ_CLEAR",   0x0_0014),
    ("LGNPU_REG_FEATURE_ID",  0x0_0018),
    ("LGNPU_REG_PERF_CYCLES", 0x0_0020),
    ("LGNPU_REG_PERF_ACTIVE", 0x0_0024),
    ("LGNPU_REG_PERF_STALL",  0x0_0028),
]

DMA_REGISTERS = [
    ("LGNPU_REG_DMA_EXT_ADDR", 0x0_0030),
    ("LGNPU_REG_DMA_LOC_ADDR", 0x0_0034),
    ("LGNPU_REG_DMA_LEN",      0x0_0038),
    ("LGNPU_REG_DMA_CTRL",     0x0_003C),
    ("LGNPU_REG_DMA_STATUS",   0x0_0040),
]

BUFFERS = [
    ("LGNPU_CMD_QUEUE_BASE",  0x0_1000),
    ("LGNPU_WEIGHT_BUF_BASE", 0x1_0000),
    ("LGNPU_ACT_BUF_BASE",    0x2_0000),
    ("LGNPU_PSUM_BUF_BASE",   0x3_0000),
]

CTRL_BITS = [
    ("LGNPU_CTRL_SOFT_RESET", 0),
    ("LGNPU_CTRL_ENABLE",     1),
]

STATUS_BITS = [
    ("LGNPU_STATUS_IDLE",       0),
    ("LGNPU_STATUS_BUSY",       1),
    ("LGNPU_STATUS_QUEUE_FULL", 2),
]

DMA_CTRL_BITS = [
    ("LGNPU_DMA_CTRL_START", 0, None),
    ("LGNPU_DMA_CTRL_DIR",   1, "0 = ext->local, 1 = local->ext"),
]

DMA_STATUS_BITS = [
    ("LGNPU_DMA_STATUS_BUSY", 0),
]

CMD_ENTRY_BYTES = 64


def generate_regs_h() -> str:
    lines: list[str] = []
    a = lines.append

    a("/* npu_regs.h - NPU register map (auto-generated by gen_headers.py) */")
    a("")
    a("#ifndef _LGNPU_REGS_H")
    a("#define _LGNPU_REGS_H")
    a("")
    a("#include <stdint.h>")
    a("")
    a("/* Register offsets */")
    for name, off in REGISTERS:
        a(f"#define {name:<28s} 0x{off:05X}U")
    a("")
    a("/* DMA engine registers */")
    for name, off in DMA_REGISTERS:
        a(f"#define {name:<28s} 0x{off:05X}U")
    a("")
    a("/* Buffer / queue bases */")
    for name, off in BUFFERS:
        a(f"#define {name:<28s} 0x{off:05X}U")
    a(f"#define {'LGNPU_CMD_ENTRY_BYTES':<28s} {CMD_ENTRY_BYTES}U")
    a("")
    a("/* CTRL register bits */")
    for name, bit in CTRL_BITS:
        a(f"#define {name:<28s} (1U << {bit})")
    a("")
    a("/* STATUS register bits */")
    for name, bit in STATUS_BITS:
        a(f"#define {name:<28s} (1U << {bit})")
    a("")
    a("/* DMA control register bits */")
    for name, bit, comment in DMA_CTRL_BITS:
        s = f"#define {name:<28s} (1U << {bit})"
        if comment:
            s += f"  /* {comment} */"
        a(s)
    a("")
    a("/* DMA status register bits */")
    for name, bit in DMA_STATUS_BITS:
        a(f"#define {name:<28s} (1U << {bit})")
    a("")
    a("#endif /* _LGNPU_REGS_H */")

    return "\n".join(lines) + "\n"


def main() -> int:
    parser = argparse.ArgumentParser(description="Generate C UAPI headers")
    parser.add_argument("--out-dir", type=Path, required=True, help="Output directory")
    args = parser.parse_args()

    args.out_dir.mkdir(parents=True, exist_ok=True)
    out = args.out_dir / "npu_regs.h"
    out.write_text(generate_regs_h())
    print(f"Generated {out}")
    return 0

if __name__ == "__main__":
    raise SystemExit(main())
