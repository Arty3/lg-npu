#!/usr/bin/env python3

"""
gen_addrmap.py - Generate npu_addrmap_pkg.sv from a declarative register map.

Usage:
    python3 tools/gen/gen_addrmap.py --out include/pkg/npu_addrmap_pkg.sv

The canonical register map lives in this script. Edit the REGISTERS and
BUFFERS tables below, then re-run to regenerate the SV package.
"""

from pathlib import Path
import argparse

# Register map definition
# (name, byte-offset hex string)
REGISTERS = [
    ("REG_CTRL",        "0_0000"),
    ("REG_STATUS",      "0_0004"),
    ("REG_DOORBELL",    "0_0008"),
    ("REG_IRQ_STATUS",  "0_000C"),
    ("REG_IRQ_ENABLE",  "0_0010"),
    ("REG_IRQ_CLEAR",   "0_0014"),
    ("REG_FEATURE_ID",  "0_0018"),
    ("REG_PERF_CYCLES", "0_0020"),
    ("REG_PERF_ACTIVE", "0_0024"),
    ("REG_PERF_STALL",  "0_0028"),
]

CMD_QUEUE_BASE = "0_1000"
CMD_ENTRY_BYTES = 64

BUFFERS = [
    ("WEIGHT_BUF_BASE", "1_0000"),
    ("ACT_BUF_BASE",    "2_0000"),
    ("PSUM_BUF_BASE",   "3_0000"),
]

CTRL_BITS = [
    ("CTRL_SOFT_RESET_BIT", 0),
    ("CTRL_ENABLE_BIT",     1),
]

STATUS_BITS = [
    ("STATUS_IDLE_BIT",   0),
    ("STATUS_BUSY_BIT",   1),
    ("STATUS_QUEUE_FULL", 2),
]

MMIO_ADDR_W = 20


def generate() -> str:
    lines: list[str] = []
    a = lines.append

    a("// ============================================================================")
    a("// npu_addrmap_pkg.sv - MMIO register / buffer address map")
    a("//   AUTO-GENERATED by tools/gen/gen_addrmap.py - do not edit by hand")
    a("// ============================================================================")
    a("package npu_addrmap_pkg;")
    a("")
    a("    import npu_types_pkg::*;")
    a("")
    a("    // ---------------------------------------------------------------")
    a("    // Register offsets (MMIO byte addresses)")
    a("    // ---------------------------------------------------------------")
    for name, offset in REGISTERS:
        a(f"    localparam logic [MMIO_ADDR_W-1:0] {name:<16s} = {MMIO_ADDR_W}'h{offset};")
    a("")
    a("    // ---------------------------------------------------------------")
    a("    // Command queue window")
    a("    // ---------------------------------------------------------------")
    a(f"    localparam logic [MMIO_ADDR_W-1:0] CMD_QUEUE_BASE  = {MMIO_ADDR_W}'h{CMD_QUEUE_BASE};")
    a(f"    localparam int                     CMD_ENTRY_BYTES  = {CMD_ENTRY_BYTES};")
    a("")
    a("    // ---------------------------------------------------------------")
    a("    // Buffer windows")
    a("    // ---------------------------------------------------------------")
    for name, offset in BUFFERS:
        a(f"    localparam logic [MMIO_ADDR_W-1:0] {name:<16s} = {MMIO_ADDR_W}'h{offset};")
    a("")
    a("    // ---------------------------------------------------------------")
    a("    // Control register bit fields")
    a("    // ---------------------------------------------------------------")
    for name, bit in CTRL_BITS:
        a(f"    localparam int {name:<20s} = {bit};")
    a("")
    a("    // ---------------------------------------------------------------")
    a("    // Status register bit fields")
    a("    // ---------------------------------------------------------------")
    for name, bit in STATUS_BITS:
        a(f"    localparam int {name:<20s} = {bit};")
    a("")
    a("endpackage : npu_addrmap_pkg")

    return "\n".join(lines) + "\n"


def main() -> int:
    parser = argparse.ArgumentParser(description="Generate SV addrmap package")
    parser.add_argument("--out", type=Path, required=True, help="Output .sv file")
    args = parser.parse_args()

    args.out.parent.mkdir(parents=True, exist_ok=True)
    args.out.write_text(generate())
    print(f"Generated {args.out}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
